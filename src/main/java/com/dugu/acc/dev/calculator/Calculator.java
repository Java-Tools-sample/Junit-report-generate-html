/**
 * Hello world!
 *
 */
public class Calculator {

	public int add(int input1, int input2) {
		return input1 + input2;
	}

	public int multiple(int input1, int input2) {
		return input1 * input2;
	}

	public int substract(int input1, int input2) {
		return input1 - input2;
	}

}
